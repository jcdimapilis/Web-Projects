<h2 style = "text-align: center;">Staff &amp; Shipments</h2>
<p>Nuevo Fresco has seven Production Staff and two Office Staff. The office is open
from Mondays thru Saturdays. Shipments are done to Zurich, Switzerland, Dubai U.A.E.,
Narita and Nagoya, Japan and London, (U.K.).</p>
<p>The current facilities in the processing plant can process 2-3 tons of Fresh Chilled tuna every week working for 12 hours. Future expansions are being eyed in the U.S.A., France and Netherlands market.
