<h2 style = "text-align: center;">Corporate Background</h2>
<p>Nuevo Fresco Marine Trading Corporation started in April 10, 2008. It is a business corporation which was set up immediately after Le Baypard Enterprise closed its operation in March 31, 2008.</p>
<p> The founding people involved inside the corporation were composed of young Christians who share the same faith and business principles.</p>

